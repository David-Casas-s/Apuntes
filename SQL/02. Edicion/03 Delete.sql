--Sintaxis basica:

DELETE FROM nameTab WHERE condition;
--Este script permite eliminar datos existentes o filas completas en una tabla especifica de una base de datos.
--En otras palabras permite borrar una o varias filas que cumplan con una condicion especifica.

--NOTA IMPORTANTE: Si no se especifica una condicion (WHERE), todos los registros en la tabla seran eliminados.

--Despues de hacer un DELETE es recomendable hacer un SELECT para verificar que los cambios se hayan realizado correctamente.
--Es posible que al hacer el select se muestre la consulta de manera desordenada.

