--Sintaxis basecia para max: 
SELECT MAX(column1) FROM nameTab

--Esta busqueda buscara SOLO el valor maximo de la columna especificada en la tabla indicada.
--No se mostrara los demás datos de la fila del valor maximo.
--Al buscar el valor maximo, se ignoraran los valores repetidos.

