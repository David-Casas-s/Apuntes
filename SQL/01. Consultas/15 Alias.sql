--Sintaxis basica:
SELECT column1 AS 'alias1', column2 AS 'alias2' FROM nameTab AS 'aliasTab';

--La funcion ALIAS se utiliza para asignar un nombre temporal a una columna o tabla en una consulta SQL.
--Esto es especialmente util para mejorar la legibilidad de los resultados o para renombrar columnas.
--AL nombrar el aligas en postgresql no se utilizan comillas simples, sino que se puede usar comillas dobles o nada en absoluto.
--Sin al nombrar el alias este posee espacios, mayuscualas o caracteres especiales, se deben utilizar comillas dobles.